package com.niit.product;

public class Furniture
{
	private String tables;
	private String chairs;
	private String drawers;
	private String stools;
	public String getTables() {
		
		return tables;
	}
	public void setTables(String tables) {
		this.tables = tables;
	}
	public String getChairs() {
		return chairs;
	}
	public void setChairs(String chairs) {
		this.chairs = chairs;
	}
	public String getDrawers() {
		return drawers;
	}
	public void setDrawers(String drawers) {
		this.drawers = drawers;
	}
	public String getStools() {
		return stools;
	}
	public void setStools(String stools) {
		this.stools = stools;
	}
	
	
	

}
